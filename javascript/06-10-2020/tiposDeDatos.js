// TIPOS DE DATOS
// Los datos de tipo texto, serán llamados CADENA o STRING
// Van entre comillas dobles o comillas simples.
"hola clase de Full Stack"
'hola clase de Full Stack'

// Los datos de tipo número, serán llamados NUMBER
// Van SIN comillas y representan un número real
3
// Los números negativos van con MENOS -
-3
// Los números decimales van con PUNTO
0.99
// Los números con miles, millones... van sin ningún símbolo
1000000
// Un número dentro de comillas dobles o simples es un TEXTO
'3'

// Los tipos de dato VERDADERO/FALSO, se llaman BOOLEAN
true
false
// El número 0 se considera FALSO en JavaScript
// El string vacío "" o '' se considera FALSO también

// Los tipos de datos SIN VALOR, se pueden escribir con UNDEFINED o NULL
// pero hay matices de diferencia:
undefined
null
// Si no he utilizado ni definido nunca un valor, es UNDEFINED
undefined
// Cuando definimos el valor y no vale NADA, tenemos NULL
null

// En JavaScript escribimos las variables, funciones... en camelCase
holaQueTalEstas
estamosEnTheBridge